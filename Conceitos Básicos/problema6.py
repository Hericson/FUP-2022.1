# Problema 6

# Suponha que você deseja preencher a seguinte ficha de inscrição de um estudante:

# Nome:

# Matrícula:

# Curso:

# Idade:

# E-mail:

# Escreva um programa que pede os dados do usuário e mostra a ficha preenchida

nome = input("Qual o seu nome? ")
matricula = input("Qual a sua matrícula? ")
curso = input("Qual o seu curso? ")
idade = int(input("Qual a sua idade? "))
email = input("Qual o seu e-mail? ")

print("Nome:", nome)
print("Matrícula:", matricula)
print("Curso:", curso)
print("Idade:", idade)
print("E-mail:", email)